<h1 class="h1">Cardboard FAQ</h1>

<div class="flex flex-col gap-8">
	<div class="block">
		<h2 class="h2">What is Cardboard?</h2>

		<p>
			Cardboard is an access manager, it currently supports Guilded. You can use it to link your
			Guilded account to Guilded Bots and other services.
		</p>
	</div>
	<div class="block">
		<h2 class="h2">Why Cardboard?</h2>

		<p>
			Cardboard is an access manager for platforms that don't have a way to link accounts. Currently
			platforms like Guilded lack one, so Cardboard is here to fill that gap. Cardboard is also
			<a
				href="https://github.com/cardboard-ink/cardboard"
				target="_blank"
				rel="noopener noreferrer"
			>
				open source
			</a>
			, so you can see exactly what it does with your data.
		</p>
	</div>
	<div class="block">
		<h2 class="h2">What can Cardboard see?</h2>

		<p>
			Cardboard can only see information on your account that can be seen by anyone on Guilded. This
			includes your username, profile picture, and bio.
		</p>
	</div>
